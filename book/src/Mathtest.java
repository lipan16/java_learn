class Mathtest{
public static void main(String[] args){
System.out.println("ceil(-2.3)="+Math.ceil(-2.3));//取上整数，不小于此数的最大整数
System.out.println("floor(2.3)="+Math.floor(2.3));//取下整数，不大于此数的最大整数
System.out.println("abs(-2.3)="+Math.abs(-2.3));//绝对值
System.out.println("max(7,9)="+Math.max(7,9));
System.out.println("min(7,9)="+Math.min(7,9));
System.out.println("sqrt(2)="+Math.sqrt(2));//开平方根
System.out.println("cbrt(2)="+Math.cbrt(2));//开立方根
System.out.println("random()="+Math.random());//0.0~~1.0的随机数
System.out.println("sin(3.14/6)="+Math.sin(3.14/6));
System.out.println("cos(3.14/6)="+Math.cos(3.14/6));
System.out.println("tan(3.14/6)="+Math.tan(3.14/6));
System.out.println("exp(1)="+Math.exp(1));//e的double次幂
System.out.println("log(2.71828)="+Math.log(2.71828));//底数是e的自然对数
System.out.println("pow(2,3)="+Math.pow(2,3));//1参数的2参数次幂
System.out.println("toDegrees(3.14/6)="+Math.toDegrees(3.14/6));//返回度数
}}